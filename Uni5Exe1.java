package Unidade5;

public class Uni5Exe1 {
    public static void main(String[] args) {
        for (int i = 1; i <= 20; i++) {
            if (i % 2 == 0) {
                System.out.println(i + " Numero par");
            } else {
                System.out.println(i + " Numero impar.");
            }
        }
    }
}
